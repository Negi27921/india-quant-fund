export function searchNseStocks(query: string, limit = 10): NseStock[] {
  if (!query || query.length < 1) return [];
  const q = query.toUpperCase();
  const exact: NseStock[] = [];
  const starts: NseStock[] = [];
  const contains: NseStock[] = [];
  for (const s of NSE_STOCKS) {
    const nm = s.name.toUpperCase();
    if (s.symbol === q || nm === q) { exact.push(s); continue; }
    if (s.symbol.startsWith(q) || nm.startsWith(q)) { starts.push(s); continue; }
    if (s.symbol.includes(q) || nm.includes(q)) { contains.push(s); }
  }
  return [...exact, ...starts, ...contains].slice(0, limit);
}
